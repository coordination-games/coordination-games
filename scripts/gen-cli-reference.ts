// ---------------------------------------------------------------------------
// Markdown rendering
// ---------------------------------------------------------------------------

const HEADER_WARNING =
  '<!-- AUTO-GENERATED by scripts/gen-cli-reference.ts. Do not edit by hand. Run `npm run gen:cli-ref` to regenerate. -->';

function escapePipes(text: string): string {
  return text.replace(/\|/g, '\\|');
}

function escapeCell(text: string): string {
  // Keep table cells single-line and pipe-safe.
  return escapePipes(text.replace(/\r?\n/g, ' ').trim());
}

function commandSynopsis(cmd: Command): string {
  // `coga <name> [options] <required> [optional...]`
  const argParts: string[] = [];
  for (const arg of getArguments(cmd)) {
    const tail = arg.variadic ? '...' : '';
    argParts.push(arg.required ? `<${arg.name()}${tail}>` : `[${arg.name()}${tail}]`);
  }
  const visibleOptions = getVisibleOptions(cmd);
  const optionPart = visibleOptions.length > 0 ? '[options]' : '';
  return ['coga', cmd.name(), optionPart, ...argParts].filter(Boolean).join(' ');
}

function getArguments(cmd: Command): CommanderArgument[] {
  // Commander v14: registeredArguments is the public API for declared args.
  const args = (cmd as Command & { registeredArguments?: CommanderArgument[] }).registeredArguments;
  return Array.isArray(args) ? args : [];
}

function getVisibleOptions(cmd: Command): CommanderOption[] {
  // Filter options the source intentionally hid by passing `undefined` as
  // description (Commander stores those as empty strings). These are
  // internal/testing flags that should not appear in user-facing docs.
  return cmd.options.filter((opt) => {
    if (opt.hidden) return false;
    if (!opt.description || opt.description.trim() === '') return false;
    return true;
  });
}

function argTypeLabel(arg: CommanderArgument): string {
  if (arg.variadic) return 'string[]';
  return 'string';
}

function defaultValueLabel(value: unknown): string {
  if (value === undefined) return '';
  if (typeof value === 'string') return `\`${value}\``;
  return `\`${JSON.stringify(value)}\``;
}

function renderArgumentsTable(cmd: Command): string {
  const args = getArguments(cmd);
  if (args.length === 0) return '';
  const lines: string[] = [
    '**Arguments**',
    '',
    '| Name | Type | Required | Description |',
    '| --- | --- | --- | --- |',
  ];
  for (const arg of args) {
    const desc = arg.description ?? '';
    const required = arg.required ? 'yes' : 'no';
    lines.push(
      `| \`${escapeCell(arg.name())}\` | ${argTypeLabel(arg)} | ${required} | ${escapeCell(desc) || '—'} |`,
    );
  }
  return lines.join('\n');
}

function renderOptionsTable(cmd: Command): string {
  const options = getVisibleOptions(cmd);
  if (options.length === 0) return '';
  const lines: string[] = [
    '**Options**',
    '',
    '| Flag | Default | Description |',
    '| --- | --- | --- |',
  ];
  // Sort options alphabetically by long flag for determinism.
  const sorted = [...options].sort((a, b) => {
    const al = (a.long ?? a.flags).toLowerCase();
    const bl = (b.long ?? b.flags).toLowerCase();
    if (al < bl) return -1;
    if (al > bl) return 1;
    return 0;
  });
  for (const opt of sorted) {
    const def = defaultValueLabel(opt.defaultValue);
    lines.push(
      `| \`${escapeCell(opt.flags)}\` | ${def || '—'} | ${escapeCell(opt.description ?? '') || '—'} |`,
    );
  }
  return lines.join('\n');
}

function renderCommand(cmd: Command): string {
  const parts: string[] = [];
  parts.push(`### \`${commandSynopsis(cmd)}\``);
  parts.push('');
  const desc = cmd.description() ?? '';
  if (desc) {
    parts.push(desc);
    parts.push('');
  }
  const argTable = renderArgumentsTable(cmd);
  if (argTable) {
    parts.push(argTable);
    parts.push('');
  }
  const optTable = renderOptionsTable(cmd);
  if (optTable) {
    parts.push(optTable);
    parts.push('');
  }
  return parts.join('\n').replace(/\n+$/, '\n');
}

function renderGroup(group: GroupSpec, byName: Map<string, Command>): string {
  const parts: string[] = [];
  parts.push(`## ${group.title}`);
  parts.push('');
  parts.push(group.intro);
  parts.push('');
  if (group.callout) {
    parts.push(group.callout);
    parts.push('');
  }
  // Sort commands alphabetically within the group for determinism.
  const sortedNames = [...group.commands].sort((a, b) => (a < b ? -1 : a > b ? 1 : 0));
  for (const name of sortedNames) {
    const cmd = byName.get(name);
    if (!cmd) {
      throw new Error(
        `gen-cli-reference: command "${name}" listed in group "${group.id}" was not found in program.commands. Available: ${[...byName.keys()].sort().join(', ')}`,
      );
    }
    parts.push(renderCommand(cmd));
  }
  return parts.join('\n').replace(/\n+$/, '\n');
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function readCliVersion(): string {
  const raw = readFileSync(CLI_PACKAGE_JSON, 'utf8');
  const parsed = JSON.parse(raw) as { version?: unknown };
  if (typeof parsed.version !== 'string' || !parsed.version) {
    throw new Error(`gen-cli-reference: missing "version" in ${CLI_PACKAGE_JSON}`);
  }
  return parsed.version;
}

function main(): void {
  // Attach the planned-but-unshipped trust commands purely for docs. This
  // does not affect the production CLI binary.
  registerTrustCommands(program);

  const byName = new Map<string, Command>();
  for (const cmd of program.commands) {
    byName.set(cmd.name(), cmd);
  }

  // Sanity check: every registered command must be claimed by exactly one
  // group, and every group entry must exist. Fail loudly if a new command
  // ships without being documented.
  const claimed = new Set<string>();
  for (const group of GROUPS) {
    for (const name of group.commands) {
      if (claimed.has(name)) {
        throw new Error(`gen-cli-reference: command "${name}" claimed by multiple groups`);
      }
      claimed.add(name);
    }
  }
  const unclaimed: string[] = [];
  for (const name of byName.keys()) {
    if (!claimed.has(name)) unclaimed.push(name);
  }
  if (unclaimed.length > 0) {
    throw new Error(
      `gen-cli-reference: commands not assigned to any group: ${unclaimed.sort().join(', ')}. Update GROUPS in scripts/gen-cli-reference.ts.`,
    );
  }

  const version = readCliVersion();

  const sections: string[] = [];
  sections.push(HEADER_WARNING);
  sections.push('');
  sections.push('# CLI Reference');
  sections.push('');
  sections.push(
    `Auto-generated reference for the \`coga\` CLI (version \`${version}\`). The CLI is the primary agent surface; the MCP server is a thin wrapper over the same command functions.`,
  );
  sections.push('');
  sections.push(
    'Run `coga --help` for a flat list, `coga <command> --help` for per-command help, or `coga help <command>` for the same.',
  );
  sections.push('');

  for (const group of GROUPS) {
    sections.push(renderGroup(group, byName));
  }

  let output = sections.join('\n').replace(/\n{3,}/g, '\n\n');
  // Guarantee a trailing newline and exactly one blank line between sections.
  if (!output.endsWith('\n')) output += '\n';

  mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, output, 'utf8');
  process.stdout.write(`gen-cli-reference: wrote ${OUTPUT_PATH}\n`);
}

main();
