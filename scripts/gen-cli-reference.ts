#!/usr/bin/env -S npx tsx
/**
 * Generate `wiki/reference/cli.md` from the live Commander tree in
 * `packages/cli/src/index.ts`.
 *
 * Run via `npm run gen:cli-ref` (also wired as a `prebuild` hook).
 *
 * The output is deterministic: same source tree → same bytes. Commands are
 * sorted alphabetically within each domain group; groups are emitted in a
 * fixed order. No timestamps, no environment-derived values beyond the CLI
 * `version` (read from `packages/cli/package.json`).
 *
 * The "trust" group lists planned-but-unimplemented stubs. Those commands
 * are not registered in the shipping CLI (see `packages/cli/src/index.ts`),
 * so this script imports `registerTrustCommands` directly and attaches them
 * to a side copy of the program purely for documentation purposes. The
 * shipping CLI is unaffected.
 */
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import type { Command, Argument as CommanderArgument, Option as CommanderOption } from 'commander';
import { registerTrustCommands } from '../packages/cli/src/commands/trust.js';
import { program } from '../packages/cli/src/index.js';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(HERE, '..');
const OUTPUT_PATH = path.join(REPO_ROOT, 'wiki', 'reference', 'cli.md');
const CLI_PACKAGE_JSON = path.join(REPO_ROOT, 'packages', 'cli', 'package.json');

// ---------------------------------------------------------------------------
// Group plan
// ---------------------------------------------------------------------------

interface GroupSpec {
  id: string;
  title: string;
  intro: string;
  commands: string[];
  /** Optional callout markdown rendered before the command list. */
  callout?: string;
}

// Confirm which trust-spec doc actually exists at write time.
function trustSpecLink(): string {
  const planPath = path.join(REPO_ROOT, 'docs', 'plans', 'trust-plugins.md');
  const oldSpec = path.join(REPO_ROOT, 'docs', 'TRUST_PLUGINS_SPEC.md');
  try {
    readFileSync(planPath);
    return 'docs/plans/trust-plugins.md';
  } catch {
    /* fall through */
  }
  try {
    readFileSync(oldSpec);
    return 'docs/TRUST_PLUGINS_SPEC.md';
  } catch {
    return 'docs/plans/trust-plugins.md';
  }
}

